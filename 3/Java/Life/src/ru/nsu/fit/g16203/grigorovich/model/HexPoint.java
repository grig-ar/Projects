package ru.nsu.fit.g16203.grigorovich.model;

public class HexPoint {
    private final double x;
    private final double y;

    public HexPoint(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}
